function dateToFractional(dateStr: string): number {
  const d = new Date(dateStr)
  const year = d.getFullYear()
  const start = new Date(year, 0, 1).getTime()
  const end = new Date(year + 1, 0, 1).getTime()
  return year + (d.getTime() - start) / (end - start)
}

/** All missions launched on or before the given fractional date */
export function getMissionsByDate(date: number): Mission[] {
  return missions.filter((m) => dateToFractional(m.launchDate) <= date)
}

/** Missions that were active at the given fractional date */
export function getActiveMissions(date: number): Mission[] {
  return missions.filter((m) => {
    const launch = dateToFractional(m.launchDate)
    const end = m.endDate ? dateToFractional(m.endDate) : 9999
    return launch <= date && end >= date
  })
}

export function getCountryScores(date: number) {
  const all = getMissionsByDate(date)
  return countries
    .map((c) => ({
      ...c,
      count: all.filter((m) => m.country === c.code).length,
    }))
    .sort((a, b) => b.count - a.count)
}

export function getContextualFact(date: number): { fact: string; missionId: string } | null {
  const year = Math.floor(date)
  const match = missions.find((m) => {
    if (!m.fact) return false
    const launchYear = new Date(m.launchDate).getFullYear()
    return launchYear === year
  })
  return match?.fact ? { fact: match.fact, missionId: match.id } : null
}
